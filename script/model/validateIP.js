export default function validateIP(IP){
  if(!IP){
    return true
  } else {
    //Courtesy of Lumina
    const regex = /^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$|^(?:[0-9a-fA-F]{1,4}:){7}[0-9a-fA-F]{1,4}|((?:[0-9a-fA-F]{1,4}:){6}|(?:[0-9a-fA-F]{1,4}:){5}|(?:[0-9a-fA-F]{1,4}:){4}|(?:[0-9a-fA-F]{1,4}:){3}|(?:[0-9a-fA-F]{1,4}:){2}|[0-9a-fA-F]{1,4}):[0-9a-fA-F]{1,4}|(?:([0-9a-fA-F]{1,4}:){5}|(?:[0-9a-fA-F]{1,4}:){4}|(?:[0-9a-fA-F]{1,4}:){3}|(?:[0-9a-fA-F]{1,4}:){2}|[0-9a-fA-F]{1,4})::[0-9a-fA-F]{1,4}|(?:([0-9a-fA-F]{1,4}:){4}|(?:[0-9a-fA-F]{1,4}:){3}|(?:[0-9a-fA-F]{1,4}:){2}|[0-9a-fA-F]{1,4})::(?:[0-9a-fA-F]{1,4}){1,2}|(?:([0-9a-fA-F]{1,4}:){3}|(?:[0-9a-fA-F]{1,4}:){2}|[0-9a-fA-F]{1,4})::(?:[0-9a-fA-F]{1,4}){1,3}|(?:([0-9a-fA-F]{1,4}:){2}|[0-9a-fA-F]{1,4})::(?:[0-9a-fA-F]{1,4}){1,4}|[0-9a-fA-F]{1,4}::(?:[0-9a-fA-F]{1,4}){1,5})$/;
      
    return regex.test(IP);
  }
}


